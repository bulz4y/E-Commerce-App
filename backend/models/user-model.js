const mongoose = require('mongoose');

const Schema = mongoose.Schema;

const userSchema = new Schema({
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    isAdmin: {
        type: Boolean,
        required: true,
        default: false
    },
    favs: [{type: Schema.Types.ObjectId, ref: 'product'}]
});

module.exports = mongoose.model('user', userSchema);